/*
Enunciado del Ejercicio:

Escriba un programa en C que implemente el algoritmo de ordenamiento QuickSort para ordenar un arreglo de números enteros.

Sugerencia: QuickSort es un algoritmo de ordenamiento eficiente que utiliza el enfoque de "divide y vencerás". Funciona seleccionando un elemento pivote y particionando el arreglo en dos subarreglos, uno con elementos menores que el pivote y otro con elementos mayores. Luego, aplica el mismo proceso de manera recursiva en los subarreglos.
*/

#include <stdio.h>

void swap(int* a, int* b) {
    int temp = *a;
    *a = *b;
    *b = temp;
}

int partition(int arr[], int low, int high) {
    int pivot = arr[high];
    int i = (low - 1);
    for (int j = low; j <= high - 1; j++) {
        if (arr[j] < pivot) {
            i++;
            swap(&arr[i], &arr[j]);
        }
    }
    swap(&arr[i + 1], &arr[high]);
    return (i + 1);
}

void quickSort(int arr[], int low, int high) {
    if (low < high) {
        int pi = partition(arr, low, high);
        quickSort(arr, low, pi - 1);
        quickSort(arr, pi + 1, high);
    }
}

void printArray(int arr[], int size) {
    for (int i = 0; i < size; i++)
        printf("%d ", arr[i]);
    printf("\n");
}

int main() {
    int arr[] = {10, 7, 8, 9, 1, 5};
    int n = sizeof(arr) / sizeof(arr[0]);
    quickSort(arr, 0, n - 1);
    printf("Sorted array: \n");
    printArray(arr, n);
    return 0;
}
